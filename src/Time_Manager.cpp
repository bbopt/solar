#include "Time_Manager.hpp"

void Time_Manager::fTimeIncrement ( void ) {
  _currentTime += _sizeOfIncrements;
  ++_incrementsCounter;
}

void Time_Manager::fResetTime ( void ) {
  _currentTime       = 0;
  _incrementsCounter = 0;
}
